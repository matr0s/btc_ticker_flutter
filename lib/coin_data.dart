const apiKey = '17671CDD-947F-48C7-B714-456F32FB41F1';

class CoinData {
  //INFO 3: Update getCoinData to take the selectedCurrency as an input.
  final String selectedCurrency;
  CoinData({this.selectedCurrency});

  Future getCoinData() async {
    //INFO 4: Update the URL to use the selectedCurrency input.
    String requestURL = '$coinAPIURL/BTC/$selectedCurrency?apikey=$apiKey';
    http.Response response = await http.get(requestURL);
    if (response.statusCode == 200) {
      var decodedData = jsonDecode(response.body);
      var lastPrice = decodedData['rate'];
      return lastPrice;
    } else {
      print(response.statusCode);
      throw 'Problem with the get request';
    }
  }
}
